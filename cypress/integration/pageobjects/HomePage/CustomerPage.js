const custdata = require("../../../fixtures/UserData/customerdata.json")
export class customer {
    customerpage() {
        cy.get(homepage).click()
        cy.get(customerpage).click()
    }

    addcustomer() {
        cy.get(addbutton).click()
        cy.get(fullname).type(custdata.fullname)
        cy.get(company).select(custdata.company)
        cy.get(contactdetailes).click()
        cy.get(mailid).type(custdata.mailid)
        cy.get(mobile).type(custdata.mobile)
        cy.get(addressdetails).click()
        cy.get(address1).type(custdata.address)
        cy.get(city).type(custdata.city)
        cy.get(state).type(custdata.state)
        cy.get(zipcode).type(custdata.zipcode)
        cy.get(country).type(custdata.country)
        cy.get(savebutton).click()
    }

    deletecustomer() {
        cy.contains('.list-row', custdata.fullname).find('input').eq(0).check()
        cy.wait(1000)
        cy.get(actionbutton).eq(1).click({ force: true })
        cy.get(deletebutton).click()
        cy.get(confirmdelete).click()
        cy.get(close).click()
    }

    editcustomer() {
        cy.contains('.list-row', custdata.fullname).find('input').eq(0).check()
        cy.wait(1000)
        cy.get(actionbutton).eq(1).click({ force: true })
        cy.get(editbutton).click()
        cy.get(field).select(custdata.field)
        cy.wait(1000)
        cy.get(value).type(custdata.name)
        cy.get(updatebutton).click()

    }

    assigntodo() {
        cy.contains('.list-row', custdata.fullname).find('input').eq(0).check()
        cy.wait(1000)
        cy.get(actionbutton).eq(1).click({ force: true })
        cy.get(assignbutton).click()
        cy.get(assignto).type(custdata.mailid)
        cy.get(comment).type(custdata.comment)
        cy.get(completedby).type(custdata.date)
        cy.get(priority).select(custdata.priority)
        cy.get(addbutton1).click()
    }

    assigntags() {
        cy.contains('.list-row', custdata.fullname).find('input').eq(0).check()
        cy.wait(1000)
        cy.get(actionbutton).eq(1).click({ force: true })
        cy.get(addtags).click()
        cy.get(tag).type(custdata.tag)
        cy.get(addbutton2).click()
    }

    printdata() {
        cy.contains('.list-row', custdata.fullname).find('input').eq(0).check()
        cy.wait(1000)
        cy.get(actionbutton).eq(1).click({ force: true })
        cy.get(print).click()
        cy.get(letterhead).click()
        cy.get(printformat).select(custdata.format)
        cy.get(printbutton).click()

    }
}